let mixedType: string | number;

let literalType: 'enable' | 'disable';

// Присвоєння значень змінним для демонстрації
mixedType = 'Hello';
console.log(mixedType); // Output: Hello

mixedType = 42;
console.log(mixedType); // Output: 42

literalType = 'enable';
console.log(literalType); // Output: enable

literalType = 'disable';
console.log(literalType); // Output: disable